import Foundation
import Cocoa

//MARK: Colors
extension NSColor {
    static var flatGray: NSColor {
        return NSColor(red: 232/255, green: 232/255, blue: 232/255, alpha: 1.0)
    }
    
    static var lightGray: NSColor {
        return NSColor(red: 242/255, green: 242/255, blue: 239/255, alpha: 1)
    }
}
